        <p>Don Joaquín, nacido el 5 de septiembre de 1884, se inició en el mundo de los negocios como criador de ganado. Más tarde se expandió a otros rubros como representante en Uruguay de distinguidas compañías estadounidenses como Goodyear Tire & Rubber, Westinghouse Electric, RCA y General Motors.</p>

        <p>Cuando joven, por iniciativa de su padre, destacado médico montevideano, pasó varios años en París, recibiendo un mínimo apoyo financiero de su propio hogar y aprendiendo a desenvolverse solo.</p>

        <p>Un internacionalista tanto por su educación como por su temperamento, hábil con los idiomas. Además del español, hablaba inglés, francés e italiano, y podía hacerse entender en algunos otros.</p>

        <p>Un conciliador natural al que, en 1941, el presidente de RI, Tom J. Davis, a petición de los rotarios de los países afectados, le solicitó actuar como moderador de un comité rotario de tres personas designado para ayudar a resolver una disputa fronteriza entre Ecuador y Perú.</p>

        <p>Joaquín fue un ferviente defensor de la extensión de Rotary, llegó al Rotary Club de Montevideo el 21 de marzo de 1921, presentado por Cornelio van Domselaar (comerciante, prominente miembro de la Asociación Británica en Uruguay, tesorero del Hospital Británico durante 18 años, miembro del directorio de la Asociación Cristiana de Jóvenes y del Instituto Crandon, además de socio fundador del Club), ocupó la secretaría en dos períodos consecutivos (1921­1923), y como Presidente en el período 1924­25. Dos veces Gobernador, en 1937­38 por el Distrito 63, y en la redistritación posterior, en 1938­39 por el Distrito 31. Inauguró un programa que generó 28 clubes nuevos en Argentina, Paraguay y Uruguay, bajo la idea de "por cada Club, otro Club". En 1941­1942 ocupó el cargo de 2do. Vicepresidente de RI, bajo la presidencia de Tom J. Davis. Ese mismo entusiasmo por la extensión fue el que llevó a su presidencia internacional, en 1953­1954, RI agregó 487 nuevos clubes en todo el mundo.</p>

        <p>También fue un líder en el servicio público en su comunidad, su país y más allá. Fue fundador de los clubes de fútbol Albion y Wanderers; presidió el Club de Tenis de Pocitos; practicó la natación en su juventud, fue jugador de golf y polo, un buen remero y jinete para montar a caballo; socio Fundador y Directivo del Club de Golf del Uruguay; miembro y representante en el Uruguay (desde 1937 a 1958) del Comité Olímpico lnternacional; Presidente de la Comisión Nacional Contra la Tuberculosis y el Cáncer de Uruguay; Director del Banco de Italia; Director de la Liga de Relaciones Culturales entre Uruguay y los Estados Unidos, entre otras.</p>

        <p>La muerte llegó a Don Joaquín el 22 de octubre de 1958, a los 74 años, encontrandosé aún en el cumplimiento de la labor rotaria como el primer día.</p>

        <p><b>Hitos de su gestión al frente de Rotary International en el período 1953­1954:</b><br>
        Aplicó por primera vez el Lema Rotario a su período. "Rotary is Hope in action" (Rotary es Esperanza en acción), siguiendo la idea de Percy C. Hodgson, Presidente de RI 1949­50, quien delineó una lista de cuatro objetivos para su mandato, entre los que se encontraban la mejor aplicación de los principios del servicio vocacional, la dedicación al entendimiento y la paz mundiales, a través de programas de servicio internacional.
        <br>
        Un record de 487 clubes rotarios fundados en todo el mundo, 19 de ellos en Japón.
        <br>
        Su sucesor en la presidencia de RI para 1954­1955, fue Herbert J. Taylor, creador y a la postre donador de los derechos de la Prueba Cuádruple.</p>

